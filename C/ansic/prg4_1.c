/* This program illustrates the Basic Data types. */
#include<stdio.h>
void main()
{       /* Declaration of Variables */
	int integer_var = 100;
	float floating_var = 331.79;
	double double_var = 8.44e+11;
	char char_var = 'W';

	/* Printing the Variables */
	printf("\ninteger_var = %i", integer_var);
	printf("\nfloating_var = %f", floating_var);
	printf("\ndouble_var = %e", double_var);
	printf("\ndouble_var = %g", double_var);
	printf("\nchar_var = %c", char_var);
}