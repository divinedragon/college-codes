/* This program adds two numbers and displays the result. */
#include<stdio.h>
void main()
{       /* Declaring the variables */
	int value1, value2, sum;

	/* Assign values and compute the result */
	value1 = 50;
	value2 = 25;
	sum = value1 + value2;

	/* Display the Result */
	printf("The sum of %i and %i is %i\n",value1, value2, sum);
}
